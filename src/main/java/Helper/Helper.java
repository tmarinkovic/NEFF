package Helper;

import java.util.Arrays;

public class Helper {

    public static void print(double[][] array) {
        System.out.println(Arrays.deepToString(array));
    }

    public static void print(double[] array) {
        System.out.println(Arrays.toString(array));
    }
    public static void print(int[] array) {
        System.out.println(Arrays.toString(array));
    }

}
